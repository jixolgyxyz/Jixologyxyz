  bi_v.descripcion AS descripcion_backlog_item_por_vencer,
  nbiv.id_proyecto AS id_proyecto_backlog_item_por_vencer,
  p_v.nombre AS nombre_proyecto_backlog_item_por_vencer,
  nbiv.fecha_vencimiento_notificada,
  CASE
    WHEN c.codigo = 'invitacion_proyecto'
      THEN ip.id_proyecto
    WHEN c.codigo = 'backlog_item_sugerencia'
      THEN bi_sug.id_proyecto
    WHEN c.codigo = 'proyecto'
      THEN np.id_proyecto
    WHEN c.codigo = 'backlog_item_proximo_vencer'
      THEN nbiv.id_proyecto
    ELSE NULL
