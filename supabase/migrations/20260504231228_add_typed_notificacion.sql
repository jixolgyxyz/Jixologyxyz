CREATE OR REPLACE FUNCTION public.crear_notificacion_invitacion_proyecto(
  p_invitacion_id integer
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_user_id integer;
  v_inv public.invitacion_proyecto%ROWTYPE;
  v_id_notificacion integer;
BEGIN
  SELECT u.id
  INTO v_user_id
  FROM public.usuario u
  WHERE u.auth_id = auth.uid();

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Usuario no autenticado.'
      USING ERRCODE = '28000';
  END IF;

  SELECT *
  INTO v_inv
  FROM public.invitacion_proyecto
  WHERE id = p_invitacion_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Invitación no encontrada.'
      USING ERRCODE = 'P0002';
  END IF;

  IF v_inv.aceptada = true THEN
    RAISE EXCEPTION 'No se puede notificar una invitación ya aceptada.'
      USING ERRCODE = '23514';
  END IF;

  IF NOT (
    v_inv.id_usuario_creador = v_user_id
    OR EXISTS (
      SELECT 1
      FROM public.usuario u
      WHERE u.id = v_user_id
        AND u.id_rol_global = ANY (ARRAY[1, 2])
    )
  ) THEN
    RAISE EXCEPTION 'No tienes permiso para crear la notificación de esta invitación.'
      USING ERRCODE = '42501';
  END IF;

  SELECT nip.id_notificacion
  INTO v_id_notificacion
  FROM public.notificacion_invitacion_proyecto nip
  WHERE nip.id_invitacion_proyecto = p_invitacion_id;

  IF v_id_notificacion IS NOT NULL THEN
    RETURN v_id_notificacion;
  END IF;

  INSERT INTO public.notificacion (
    nombre,
    descripcion,
    id_usuario,
    id_tipo_notificacion
  )
  VALUES (
    'Invitación a proyecto',
    LEFT(COALESCE(v_inv.descripcion, 'Tienes una nueva invitación a proyecto.'), 100),
    v_inv.id_usuario_destino,
    2
  )
  RETURNING id INTO v_id_notificacion;

  INSERT INTO public.notificacion_invitacion_proyecto (
    id_notificacion,
    id_invitacion_proyecto
  )
  VALUES (
    v_id_notificacion,
    p_invitacion_id
  );

  RETURN v_id_notificacion;
END;
$$;

CREATE OR REPLACE FUNCTION public.crear_notificaciones_sugerencia_backlog_item(
  p_id_backlog_item bigint,
  p_id_usuario_actor integer DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_backlog public.backlog_item%ROWTYPE;
  v_id_backlog_item_suscrito bigint;
  v_id_notificacion integer;
  v_total integer := 0;
  v_recipient record;
BEGIN
  SELECT bi.*
  INTO v_backlog
  FROM public.backlog_item bi
  JOIN public.backlog_item_sugerencia_creacion bis
    ON bis.id = bi.id
  WHERE bi.id = p_id_backlog_item;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'No existe sugerencia de backlog item con id %.', p_id_backlog_item
      USING ERRCODE = 'P0002';
  END IF;

  v_id_backlog_item_suscrito := COALESCE(
    v_backlog.id_backlog_item_padre,
    v_backlog.id
  );

  FOR v_recipient IN
    SELECT DISTINCT s.id_usuario
    FROM public.suscripcion_notificacion_backlog_item s
    JOIN public.usuario u
      ON u.id = s.id_usuario
    WHERE s.id_backlog_item = v_id_backlog_item_suscrito
      AND u.activo = true
      AND s.id_usuario IS DISTINCT FROM p_id_usuario_actor
  LOOP
    IF NOT EXISTS (
      SELECT 1
      FROM public.notificacion n
      JOIN public.notificacion_backlog_item_sugerencia nbis
        ON nbis.id_notificacion = n.id
      WHERE n.id_usuario = v_recipient.id_usuario
        AND nbis.id_backlog_item = p_id_backlog_item
    ) THEN
      INSERT INTO public.notificacion (
        nombre,
        descripcion,
        id_usuario,
        id_tipo_notificacion
      )
      VALUES (
        'Nueva sugerencia de backlog item',
        'Hay una sugerencia relacionada con un backlog item que sigues.',
        v_recipient.id_usuario,
        3
      )
      RETURNING id INTO v_id_notificacion;

      INSERT INTO public.notificacion_backlog_item_sugerencia (
        id_notificacion,
        id_backlog_item
      )
      VALUES (
        v_id_notificacion,
        p_id_backlog_item
      );

      v_total := v_total + 1;
    END IF;
  END LOOP;

  RETURN v_total;
END;
$$;

CREATE OR REPLACE FUNCTION public.crear_notificaciones_proyecto(
  p_id_proyecto integer,
  p_codigo_seccion varchar,
  p_nombre varchar,
  p_descripcion varchar DEFAULT NULL,
  p_id_usuario_actor integer DEFAULT NULL
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_id_seccion smallint;
  v_id_notificacion integer;
  v_total integer := 0;
  v_recipient record;
BEGIN
  SELECT s.id
  INTO v_id_seccion
  FROM public.catalogo_seccion_proyecto_notificacion s
  WHERE s.codigo = p_codigo_seccion
    AND s.activo = true;

  IF v_id_seccion IS NULL THEN
    RAISE EXCEPTION 'Sección de proyecto inválida: %', p_codigo_seccion
      USING ERRCODE = '23503';
  END IF;

  IF NOT EXISTS (
    SELECT 1
    FROM public.proyecto p
    WHERE p.id = p_id_proyecto
  ) THEN
    RAISE EXCEPTION 'Proyecto no encontrado: %', p_id_proyecto
      USING ERRCODE = 'P0002';
  END IF;

  FOR v_recipient IN
    SELECT DISTINCT u.id AS id_usuario
    FROM public.usuario u
    JOIN public.usuario_proyecto up
      ON up.id_usuario = u.id
    WHERE up.id_proyecto = p_id_proyecto
      AND u.activo = true
      AND (
        p_id_usuario_actor IS NULL
        OR u.id IS DISTINCT FROM p_id_usuario_actor
      )
  LOOP
    INSERT INTO public.notificacion (
      nombre,
      descripcion,
      id_usuario,
      id_tipo_notificacion
    )
    VALUES (
      LEFT(COALESCE(NULLIF(p_nombre, ''), 'Actualización de proyecto'), 50),
      LEFT(p_descripcion, 100),
      v_recipient.id_usuario,
      4
    )
    RETURNING id INTO v_id_notificacion;

    INSERT INTO public.notificacion_proyecto (
      id_notificacion,
      id_proyecto,
      id_seccion_proyecto,
      id_usuario_actor
    )
    VALUES (
      v_id_notificacion,
      p_id_proyecto,
      v_id_seccion,
      p_id_usuario_actor
    );

    v_total := v_total + 1;
  END LOOP;

  RETURN v_total;
END;
$$;

CREATE OR REPLACE FUNCTION public.generar_notificaciones_backlog_items_proximos_vencer(
  p_horas_anticipacion integer DEFAULT 24
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_total integer := 0;
  v_id_notificacion integer;
  v_item record;
  v_recipient record;
BEGIN
  IF p_horas_anticipacion IS NULL OR p_horas_anticipacion <= 0 THEN
    RAISE EXCEPTION 'p_horas_anticipacion debe ser mayor a 0.'
      USING ERRCODE = '22023';
  END IF;

  FOR v_item IN
    SELECT
      bi.id,
      bi.nombre,
      bi.descripcion,
      bi.fecha_vencimiento,
      bi.id_proyecto,
      bi.id_usuario_responsable,
      p.nombre AS nombre_proyecto
    FROM public.backlog_item bi
    JOIN public.proyecto p
      ON p.id = bi.id_proyecto
    WHERE bi.fecha_vencimiento IS NOT NULL
      AND bi.fecha_vencimiento > now()
      AND bi.fecha_vencimiento <= now() + make_interval(hours => p_horas_anticipacion)
      AND bi.es_terminal = false
  LOOP
    FOR v_recipient IN
      SELECT DISTINCT x.id_usuario
      FROM (
        SELECT v_item.id_usuario_responsable AS id_usuario
        WHERE v_item.id_usuario_responsable IS NOT NULL

        UNION

        SELECT s.id_usuario
        FROM public.suscripcion_notificacion_backlog_item s
        WHERE s.id_backlog_item = v_item.id
      ) x
      JOIN public.usuario u
        ON u.id = x.id_usuario
      WHERE u.activo = true
        AND (
          EXISTS (
            SELECT 1
            FROM public.usuario_proyecto up
            WHERE up.id_usuario = x.id_usuario
              AND up.id_proyecto = v_item.id_proyecto
          )
          OR u.id_rol_global = ANY (ARRAY[1, 2])
        )
    LOOP
      INSERT INTO public.notificacion_backlog_item_vencimiento_registro (
        id_usuario,
        id_backlog_item,
        fecha_vencimiento_notificada
      )
      VALUES (
        v_recipient.id_usuario,
        v_item.id,
        v_item.fecha_vencimiento
      )
      ON CONFLICT DO NOTHING;

      IF FOUND THEN
        INSERT INTO public.notificacion (
          nombre,
          descripcion,
          id_usuario,
          id_tipo_notificacion
        )
        VALUES (
          'Backlog item por vencer',
          LEFT('"' || v_item.nombre || '" vence en las próximas ' || p_horas_anticipacion::text || ' horas.', 100),
          v_recipient.id_usuario,
          5
        )
        RETURNING id INTO v_id_notificacion;

        INSERT INTO public.notificacion_backlog_item_vencimiento (
          id_notificacion,
          id_backlog_item,
          id_proyecto,
          fecha_vencimiento_notificada
        )
        VALUES (
          v_id_notificacion,
          v_item.id,
          v_item.id_proyecto,
          v_item.fecha_vencimiento
        );

        UPDATE public.notificacion_backlog_item_vencimiento_registro
        SET id_notificacion = v_id_notificacion
        WHERE id_usuario = v_recipient.id_usuario
          AND id_backlog_item = v_item.id
          AND fecha_vencimiento_notificada = v_item.fecha_vencimiento;

        v_total := v_total + 1;
      END IF;
    END LOOP;
  END LOOP;

  RETURN v_total;
END;
$$;

CREATE OR REPLACE FUNCTION public.generar_notificaciones_sprints_proximos_vencer(
  p_horas_anticipacion integer DEFAULT 24
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_total integer := 0;
  v_id_notificacion integer;
  v_sprint record;
  v_recipient record;
  v_id_seccion_sprints smallint;
BEGIN
  IF p_horas_anticipacion IS NULL OR p_horas_anticipacion <= 0 THEN
    RAISE EXCEPTION 'p_horas_anticipacion debe ser mayor a 0.'
      USING ERRCODE = '22023';
  END IF;

  SELECT s.id
  INTO v_id_seccion_sprints
  FROM public.catalogo_seccion_proyecto_notificacion s
  WHERE s.codigo = 'sprints'
    AND s.activo = true;

  IF v_id_seccion_sprints IS NULL THEN
    RAISE EXCEPTION 'No existe sección activa de proyecto con código sprints.'
      USING ERRCODE = '23503';
  END IF;

  FOR v_sprint IN
    SELECT
      s.id,
      s.nombre,
      s.objetivo,
      s.fecha_final,
      s.id_proyecto,
      p.nombre AS nombre_proyecto
    FROM public.sprint s
    JOIN public.proyecto p
      ON p.id = s.id_proyecto
    JOIN public.estatus_sprint es
      ON es.id = s.id_estatus
    WHERE s.fecha_final IS NOT NULL
      AND s.fecha_final > now()
      AND s.fecha_final <= now() + make_interval(hours => p_horas_anticipacion)
      AND es.es_terminal = false
  LOOP
    FOR v_recipient IN
      SELECT DISTINCT u.id AS id_usuario
      FROM public.usuario u
      JOIN public.usuario_proyecto up
        ON up.id_usuario = u.id
      WHERE up.id_proyecto = v_sprint.id_proyecto
        AND u.activo = true
    LOOP
      INSERT INTO public.notificacion_sprint_vencimiento_registro (
        id_usuario,
        id_sprint,
        fecha_final_notificada
      )
      VALUES (
        v_recipient.id_usuario,
        v_sprint.id,
        v_sprint.fecha_final
      )
      ON CONFLICT DO NOTHING;

      IF FOUND THEN
        INSERT INTO public.notificacion (
          nombre,
          descripcion,
          id_usuario,
          id_tipo_notificacion
        )
        VALUES (
          'Sprint por finalizar',
          LEFT('"' || v_sprint.nombre || '" finaliza en las próximas ' || p_horas_anticipacion::text || ' horas.', 100),
          v_recipient.id_usuario,
          4
        )
        RETURNING id INTO v_id_notificacion;

        INSERT INTO public.notificacion_proyecto (
          id_notificacion,
          id_proyecto,
          id_seccion_proyecto,
          id_usuario_actor
        )
        VALUES (
          v_id_notificacion,
          v_sprint.id_proyecto,
          v_id_seccion_sprints,
          NULL
        );

        UPDATE public.notificacion_sprint_vencimiento_registro
        SET id_notificacion = v_id_notificacion
        WHERE id_usuario = v_recipient.id_usuario
          AND id_sprint = v_sprint.id
          AND fecha_final_notificada = v_sprint.fecha_final;

        v_total := v_total + 1;
      END IF;
    END LOOP;
  END LOOP;

  RETURN v_total;
END;
$$;

