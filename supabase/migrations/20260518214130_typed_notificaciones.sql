-- Catálogos base del sistema de notificaciones
CREATE TABLE IF NOT EXISTS public.catalogo_tipo_notificacion (
  id smallint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo character varying(50) NOT NULL UNIQUE,
  nombre character varying(80) NOT NULL,
  descripcion character varying(250),
  activo boolean DEFAULT true NOT NULL
);

CREATE TABLE IF NOT EXISTS public.catalogo_seccion_proyecto_notificacion (
