DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'notificacion_id_id_tipo_notificacion_key'
      AND conrelid = 'public.notificacion'::regclass
  ) THEN
    ALTER TABLE public.notificacion
      ADD CONSTRAINT notificacion_id_id_tipo_notificacion_key UNIQUE (id, id_tipo_notificacion);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'notificacion_leida_fecha_check'
      AND conrelid = 'public.notificacion'::regclass
  ) THEN
    ALTER TABLE public.notificacion
      ADD CONSTRAINT notificacion_leida_fecha_check
      CHECK (
        (leida = false AND fecha_lectura IS NULL)
        OR
        (leida = true AND fecha_lectura IS NOT NULL)
      ) NOT VALID;
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'notificacion_id_tipo_notificacion_fkey'
      AND conrelid = 'public.notificacion'::regclass
  ) THEN
    ALTER TABLE public.notificacion
      ADD CONSTRAINT notificacion_id_tipo_notificacion_fkey
      FOREIGN KEY (id_tipo_notificacion)
      REFERENCES public.catalogo_tipo_notificacion(id);
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'notificacion_id_usuario_fkey'
      AND conrelid = 'public.notificacion'::regclass
  ) THEN
    ALTER TABLE public.notificacion
      ADD CONSTRAINT notificacion_id_usuario_fkey
      FOREIGN KEY (id_usuario)
      REFERENCES public.usuario(id)
      DEFERRABLE;
  END IF;
END $$;

