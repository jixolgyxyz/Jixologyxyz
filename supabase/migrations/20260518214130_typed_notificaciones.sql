-- Funciones para el frontend: marcar leída/no leída sin permisos privilegiados
CREATE OR REPLACE FUNCTION public.marcar_notificacion_como_leida(p_id_notificacion integer)
RETURNS public.notificacion
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path TO ''
AS $$
DECLARE
  v_notificacion public.notificacion;
BEGIN
  IF auth.uid() IS NULL THEN
    RAISE EXCEPTION 'Usuario no autenticado.'
      USING ERRCODE = '28000';
  END IF;

  UPDATE public.notificacion n
  SET leida = true,
      fecha_lectura = COALESCE(n.fecha_lectura, now())
  WHERE n.id = p_id_notificacion
    AND EXISTS (
      SELECT 1
      FROM public.usuario u
      WHERE u.id = n.id_usuario
        AND u.auth_id = auth.uid()
    )
  RETURNING n.* INTO v_notificacion;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Notificación no encontrada o no permitida.'
      USING ERRCODE = '42501';
  END IF;

  RETURN v_notificacion;
END;
$$;

