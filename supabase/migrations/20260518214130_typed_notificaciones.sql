-- Tablas detalle con foreign keys reales por tipo de notificación
CREATE TABLE IF NOT EXISTS public.notificacion_invitacion_proyecto (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (2::smallint) STORED,
  id_invitacion_proyecto integer NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacion_proyecto (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (4::smallint) STORED,
  id_proyecto integer NOT NULL,
  id_seccion_proyecto smallint NOT NULL,
  id_usuario_actor integer
);

CREATE TABLE IF NOT EXISTS public.notificacion_backlog_item_sugerencia (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (3::smallint) STORED,
  id_backlog_item bigint NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacion_backlog_item_vencimiento (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (5::smallint) STORED,
  id_backlog_item bigint NOT NULL,
  id_proyecto integer NOT NULL,
  fecha_vencimiento_notificada timestamp with time zone NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacion_backlog_item_cambio (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (6::smallint) STORED,
  id_backlog_item bigint NOT NULL,
  id_proyecto integer NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacion_backlog_item_creacion (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (7::smallint) STORED,
  id_backlog_item bigint NOT NULL,
  id_proyecto integer NOT NULL
);

CREATE TABLE IF NOT EXISTS public.notificacion_sprint_vencimiento (
  id_notificacion integer NOT NULL PRIMARY KEY,
  id_tipo_notificacion smallint GENERATED ALWAYS AS (8::smallint) STORED,
  id_sprint bigint NOT NULL,
  id_proyecto integer NOT NULL,
  fecha_final_notificada timestamp with time zone NOT NULL
);

-- Registros de vencimiento para no repetir notificaciones temporales
CREATE TABLE IF NOT EXISTS public.notificacion_backlog_item_vencimiento_registro (
  id_usuario integer NOT NULL,
  id_backlog_item bigint NOT NULL,
  fecha_vencimiento_notificada timestamp with time zone NOT NULL,
  id_notificacion integer,
  fecha_registro timestamp with time zone DEFAULT now() NOT NULL,
  PRIMARY KEY (id_usuario, id_backlog_item, fecha_vencimiento_notificada)
);

CREATE TABLE IF NOT EXISTS public.notificacion_sprint_vencimiento_registro (
  id_usuario integer NOT NULL,
  id_sprint bigint NOT NULL,
  fecha_final_notificada timestamp with time zone NOT NULL,
  id_notificacion integer,
  fecha_registro timestamp with time zone DEFAULT now() NOT NULL,
  PRIMARY KEY (id_usuario, id_sprint, fecha_final_notificada)
);

