-- Tabla principal de notificaciones
CREATE TABLE IF NOT EXISTS public.notificacion (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre character varying(50) NOT NULL,
  descripcion character varying(100),
  leida boolean DEFAULT false NOT NULL,
  fecha_lectura timestamp with time zone,
  id_usuario integer NOT NULL,
  fecha_envio timestamp with time zone DEFAULT now() NOT NULL,
  id_tipo_notificacion smallint DEFAULT 1 NOT NULL
);

ALTER TABLE public.notificacion ADD COLUMN IF NOT EXISTS id_tipo_notificacion smallint;
ALTER TABLE public.notificacion ALTER COLUMN id_tipo_notificacion SET DEFAULT 1;

UPDATE public.notificacion n
SET id_tipo_notificacion = 1
WHERE n.id_tipo_notificacion IS NULL;

UPDATE public.notificacion n
SET id_tipo_notificacion = 1
WHERE NOT EXISTS (
  SELECT 1
  FROM public.catalogo_tipo_notificacion c
  WHERE c.id = n.id_tipo_notificacion
);

-- Normalización de tipos para detalles existentes antes de reforzar llaves compuestas
DO $$
BEGIN
  IF to_regclass('public.notificacion_invitacion_proyecto') IS NOT NULL THEN
    UPDATE public.notificacion n
    SET id_tipo_notificacion = 2
    FROM public.notificacion_invitacion_proyecto d
    WHERE d.id_notificacion = n.id;
  END IF;

  IF to_regclass('public.notificacion_backlog_item_sugerencia') IS NOT NULL THEN
    UPDATE public.notificacion n
    SET id_tipo_notificacion = 3
    FROM public.notificacion_backlog_item_sugerencia d
    WHERE d.id_notificacion = n.id;
  END IF;

  IF to_regclass('public.notificacion_proyecto') IS NOT NULL THEN
    UPDATE public.notificacion n
    SET id_tipo_notificacion = 4
    FROM public.notificacion_proyecto d
    WHERE d.id_notificacion = n.id;
  END IF;

  IF to_regclass('public.notificacion_backlog_item_vencimiento') IS NOT NULL THEN
    UPDATE public.notificacion n
    SET id_tipo_notificacion = 5
    FROM public.notificacion_backlog_item_vencimiento d
    WHERE d.id_notificacion = n.id;
  END IF;
END $$;

